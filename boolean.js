// Boolean values in JavaScript
// Boolean values can be either true or false
const isMarried = true;
const isSingle = false;
// Boolean values are often used in logical operations
const isAdult = true;
const isStudent = false;
// Boolean values can be used in comparisons
const isEqual = (5 === 5); // true
const isNotEqual = (5 !== 10); // true
// Boolean values can be used in logical operations
const isTrue = (isAdult && isStudent); // false
const isFalse = (isAdult || isStudent); // true
// Boolean values can be used in conditional statements
if (isMarried) {
  console.log("You are married.");
} else {
  console.log("You are not married.");
}
// Boolean values are often used in conditional statements
console.log(isMarried);